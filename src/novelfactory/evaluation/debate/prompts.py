#  Critic 评审 Prompt（v7.3 新增）
#  受 MAD-Fact (中科院 2025, arXiv:2510.22967) 多角色辩论设计启发。
#  差异说明：
#    - MAD-Fact 面向事实验证（6 角色并行 + Judge 多数投票）
